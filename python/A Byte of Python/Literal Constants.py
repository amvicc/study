#!/usr/bin/env python3

# Numbers in python:
# Integer number in python:
a = 2
print(a)
print(type(a))
# Float number in python:
b = 3.233
print(b)
print(type(b))
c = 3233E-3
print(c)
print(type(c))
# Complex number in python:
d = -17 + 3j
print(d)
print(type(d))

# String in python:
str1 = 'Type of string one'  # Using single Quotes
print(str1)

str2 = "String using Double Quotes"  # Using double Quotes
print(str2)

str3 = '''This is multi-line string 
Second line
Third line
"Quotes in Quotes"
Last line'''  # Using triple Quotes
print(str3)